public class Task4 {
    public static void main(String[] args) {
        // Area of a circle = pi * radius * radius
        double area = 3.14; //The value of pi
        double radius = 12.7;

        //Calculate the area of a circle using compound operator
        area *= radius * radius;
        System.out.println("The area of the circle is " + area + "meters.");
    }
}
